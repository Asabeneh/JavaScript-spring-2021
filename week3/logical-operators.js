console.log('We are learning logical operators')

// to get true both statements should be true
console.log(2 > 1 && 4 < 5)
console.log(2 < 1 && 4 < 5)
console.log(2 < 1 && 4 > 5)

// to get true , just only one statement should be true
console.log(2 > 1 || 4 < 5)
console.log(2 > 1 || 4 > 5)
console.log(2 < 1 || 4 > 5)

